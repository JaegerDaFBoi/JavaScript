//Operadores aritmeticos
let a = 3, b = 4;
let z = a + b;
console.log("Resultado: " + z); //Suma

z = a - b;
console.log("Resultado: " + z); //Resta

z = a * b;
console.log("Resultado: " + z); //Multiplicacion

z = a / b;
console.log("Resultado: " + z); //Division

//Calculo del modulo
z = a % b;
console.log("Resultado: " + z);

//Exponencial
z = a ** b;
console.log("Resultado: " + z);
 